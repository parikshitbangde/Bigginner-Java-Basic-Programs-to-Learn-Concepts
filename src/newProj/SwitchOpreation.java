package newProj;

public class SwitchOpreation {
	public static void main (String args[]) {
		 int i = 5;
		 switch (i) {
		 case 1:
			 System.out.println("one");
			 break;
		 case 2:
			 System.out.println("Two");
			 break;
		 case 3:
			 System.out.println("Three");
			 break;
		 default:
			 System.out.println("Number is above Three");
		 }
	}
	

}
